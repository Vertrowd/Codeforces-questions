#include <iostream>
#include <vector>
using namespace std;
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }
    vector<int> v1(n + 1);
    v1[0] = 1;
    int sum = 1;
    for (int i = 1; i < v1.size(); i++)
    {
        sum += arr[i - 1];
        v1[i] = sum;
    }

    
    int m;
    cin >> m;
    vector<int> v2(m);
    for (int i = 0; i < m; i++)
    {
        cin >> v2[i];
    }
    for (int i = 0; i < m; i++)
    {
        int index;
        int tar = v2[i];
        int left = 0;
        int right = v1.size()-1;
        while (left <= right)
        {
            int mid = (left + right) / 2;
            if (tar < v1[mid])
            {
                if (tar < v1[mid] && tar >= v1[mid - 1])
                {
                    index = mid;
                    break;
                }
                else
                {
                    right = mid - 1;
                }
            }
            else if (tar >= v1[mid])
            {
                if (tar >= v1[mid] && tar < v1[mid + 1])
                {
                    index = mid + 1;
                    break;
                }
                else
                {
                    left = mid + 1;
                    
                }
            }
        }
        cout << index << "\n";
    }

    return 0;
}